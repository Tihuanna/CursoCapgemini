
public class Literal {
	
	public static void main(String []args) {
		
		int numero;
		String cadeiaCaracteres;
		char caractere;
		boolean certoOuErrado;
		
		numero = 10;
		cadeiaCaracteres = "Meu curso de java";
		caractere = 'S';
		certoOuErrado = true;
		
		System.out.println("O valor de numero é "+numero);
		System.out.println("O valor de cadeia é "+cadeiaCaracteres);
		System.out.println("O valor de caractere é "+caractere);
		System.out.println("O valor de certo ou errado é " + certoOuErrado);
	}

}
